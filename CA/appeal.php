            Income Tax Appellate Tribunal (ITAT) is a quasi-judicial authority to file appeals against the orders
             of income tax authorities. A tax appeal can be filed by a taxpayer who does not agree with the 
             assessment order or any other order, passed by an income-tax authority. Similarly, 
            an income-tax authority can also file an appeal against any order passed by lower income-tax authorities.
            The ITAT is a quasi-judicial body set up by the Central Government to deal with appellate
             matters under the Income Tax Act, 1961. The ITAT functions under the Ministry of Law and Justice. 
             There are different benches of the ITAT for different regions having regional jurisdiction. The President
              of the ITAT constitutes a bench of an ITAT from amongst the members of 
            the ITAT. Each bench shall consist of an accountant member and a judicial member.

However, the President or any member of the ITAT may dispose of any appeal, where the total 
income as calculated by the Assessing Officer does not exceed Rs 50 lakh. In cases where required, 
the ITAT may constitute a special bench with three or more members to dispose of appeals, with at 
least one accountant member and one judicial member. The ITAT is also the final fact-finding authority.
 Any decision of the ITAT is final when it concerns factual matters.
